function filterAndRender() {
  const roundFilter = document.getElementById("roundSelect").value;
  const weightFilter = document.getElementById("weightSelect").value;
  const schoolFilter = document.getElementById("schoolSelect").value;
  const searchInput = document.getElementById("searchInput").value.toLowerCase();
  const resultsContainer = document.getElementById("resultsContainer");

  // If no dropdown filter is selected, show nothing
  if (roundFilter === "All" && weightFilter === "All" && schoolFilter === "All" && searchInput === "") {
    resultsContainer.innerHTML = "<p class='italic text-gray-500'>Select a filter to view match results.</p>";
    return;
  }


  let filtered = resultData.filter(match => {
    return (
      (roundFilter === "All" || match.round === roundFilter) &&
      (weightFilter === "All" || match.weight === weightFilter) &&
      (schoolFilter === "All" || match.winnerSchool === schoolFilter || match.loserSchool === schoolFilter) &&
      (
        match.winner.toLowerCase().includes(searchInput) ||
        match.loser.toLowerCase().includes(searchInput) ||
        match.winnerSchool.toLowerCase().includes(searchInput) ||
        match.loserSchool.toLowerCase().includes(searchInput)
      )
    );
  });

  resultsContainer.innerHTML = "";
  if (filtered.length === 0) {
    resultsContainer.innerHTML = "<p class='italic text-gray-500'>No results found.</p>";
  }

  const grouped = {};
  filtered.forEach(match => {
    if (!grouped[match.round]) grouped[match.round] = [];
    grouped[match.round].push(match);
  });

  const sortedRounds = Object.keys(grouped).sort();

  sortedRounds.forEach(round => {
    resultsContainer.innerHTML += `<h3 class='text-sm font-bold text-blue-800 mt-4 mb-1'>${round}</h3>`;
    grouped[round]
      .sort((a, b) => parseInt(a.bout) - parseInt(b.bout))
      .forEach(match => {
        const matchHtml = `
          <p class="ml-4 text-sm">${match.bout}. ${match.weight}  ${match.winner} (${match.winnerSchool}) defeated ${match.loser} (${match.loserSchool}) [${match.result}]</p>
        `;
        resultsContainer.innerHTML += matchHtml;
      });
  });
}

function updateTeamStats() {
  const selectedTeam = document.getElementById("teamSelect").value;
  const container = document.getElementById("teamStats");

  if (selectedTeam === "All") {
    container.innerHTML = "";
    return;
  }

  const stats = {};

  resultData.forEach(match => {
    const schools = [match.winnerSchool, match.loserSchool];

    if (!stats[selectedTeam]) {
      stats[selectedTeam] = {
        wins: 0,
        losses: 0,
        falls: 0,
        techs: 0,
        majors: 0
      };
    }

    if (match.winnerSchool === selectedTeam) stats[selectedTeam].wins++;
    if (match.loserSchool === selectedTeam) stats[selectedTeam].losses++;

    const resultText = match.result.toLowerCase();
    if (resultText.includes("fall")) stats[selectedTeam].falls++;
    else if (resultText.includes("tf") || resultText.includes("tech")) stats[selectedTeam].techs++;
    else if (resultText.includes("md") || resultText.includes("major")) stats[selectedTeam].majors++;
  });

  let html = "<h2 class='text-base font-bold mb-1'> Team Stats</h2>";
  if (!stats[selectedTeam]) {
    html += "<p class='italic text-gray-500'>No stats available for this team.</p>";
  } else {
    const s = stats[selectedTeam];
    html += `<p class='text-sm'><strong>${selectedTeam}</strong>: ${s.wins}W - ${s.losses}L, ${s.falls} falls, ${s.techs} techs, ${s.majors} majors</p>`;
  }

  container.innerHTML = html;
}

